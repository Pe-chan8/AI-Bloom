module TopHelper
  def sample_message_for(buddy)
    case buddy&.code
    when "analytical"
      "今日は起きたことを少し整理してみませんか。事実と気持ちを分けて眺めてみると、新しい気づきが見えてくるかもしれません。"
    when "amiable"
      "今日も人のためにたくさん気を配りましたね!その優しさはちゃんと周りに届いていますよ。自分の気持ちも、そっといたわってあげましょう。"
    when "driving"
      "ここまで進めてきた行動だけでも十分すごいことです。もしできそうなら、今日は「これだけやる」と決めて一歩だけ進んでみましょう。"
    when "expressive"
      "今日あったことの中で、心が少し動いた瞬間を一つだけ思い出してみませんか。その気持ちを言葉にしてみると、新しいアイデアが生まれるかもしれません!"
    when "kansai_friend"
      # 関西の友達バディ用（神戸寄り・やわらかめ）
      "今日もここまでおつかれさん。ちょっとずつでも、ちゃんと前に進めてるで!気づいたこととか「これ良かったな」って思ったこと、軽くメモしとこか。"
    else
      # normal
      "今日もここまでお疲れさまでした。小さな一歩でも、ちゃんと前に進んでいますよ。気づいたことや感じたことを、そっとメモしてみましょう。"
    end
  end
end
